package com.model;

public class Commissioner {

	private int id;
	private String name;
	private boolean hasRightToArrest;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public boolean isHasRightToArrest() {
		return hasRightToArrest;
	}
	public void setHasRightToArrest(boolean hasRightToArrest) {
		this.hasRightToArrest = hasRightToArrest;
	}
	
}
